package com.gentech.String;

public class NumberOfchar {
		    public static void main(String[] args) {
		        String s = "bhavya";
		        int count = 0;
		        for (char c : s.toCharArray()) {
		        	System.out.println(c);
		            count++;
		        }
		        System.out.println("Length of the string is: " + count);
	
		
	}

}
